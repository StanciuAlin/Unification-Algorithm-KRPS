
def print_header(header: str, domain: str = "KRPS", extra: str = ""):
    print("\n" + "=" * 75)
    print(header)
    print(f"\n\t\t{domain}")
    print(f"\t\t{extra}")
    print("=" * 75 + "\n")


def print_title(title: str):
    print("\n" + "=" * 75)
    print(title)
    print("=" * 75 + "\n")
